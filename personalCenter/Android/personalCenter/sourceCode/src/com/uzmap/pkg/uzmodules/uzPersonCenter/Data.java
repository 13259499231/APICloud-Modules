package com.uzmap.pkg.uzmodules.uzPersonCenter;

import android.graphics.drawable.BitmapDrawable;

public class Data {
	
	private BitmapDrawable bgImg;
	
	private BitmapDrawable selectedImg;
	
	private BitmapDrawable lightImg;
	
	private String title;
	
	private String count;
	
	private String titleColor;
	
	private String titleLightColor;
	
	private String countColor;
	
	private String countLightColor;

	
	public BitmapDrawable getBgImg() {
		return bgImg;
	}

	public void setBgImg(BitmapDrawable bgImg) {
		this.bgImg = bgImg;
	}

	public BitmapDrawable getSelectedImg() {
		return selectedImg;
	}

	public void setSelectedImg(BitmapDrawable selectedImg) {
		this.selectedImg = selectedImg;
	}

	public BitmapDrawable getLightImg() {
		return lightImg;
	}

	public void setLightImg(BitmapDrawable lightImg) {
		this.lightImg = lightImg;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public String getTitleColor() {
		return titleColor;
	}

	public void setTitleColor(String titleColor) {
		this.titleColor = titleColor;
	}

	public String getTitleLightColor() {
		return titleLightColor;
	}

	public void setTitleLightColor(String titleLightColor) {
		this.titleLightColor = titleLightColor;
	}

	public String getCountColor() {
		return countColor;
	}

	public void setCountColor(String countColor) {
		this.countColor = countColor;
	}

	public String getCountLightColor() {
		return countLightColor;
	}

	public void setCountLightColor(String countLightColor) {
		this.countLightColor = countLightColor;
	}
	
	

}
